/**
 * token.ts
 *
 * 定義白話文的 Token 與相關 Enum
 */

export enum TokenType {
  // 關鍵字
  主程式,
  宣告,
  整數,
  為,
  若,
  否則,
  當,
  結束,
  函式,
  回傳,
  輸出,

  // 運算子 / 符號
  等號, // =
  加, // +
  減, // -
  乘, // *
  除, // /
  加等於, // +=
  減等於, // -=

  // 比較運算
  等於, // ==
  不等於, // !=
  大於, // >
  小於, // <
  大於等於, // >=
  小於等於, // <=

  左括號, // (
  右括號, // )
  冒號, // :
  逗號, // ,
  註解, // //

  // 識別字 / 數值 / 字串
  識別符,
  整數常量,
  字串常量,

  // 其他
  EOF,
}

export interface Token {
  type: TokenType;
  lexeme: string; // 原字面值
  line: number; // 來源碼的行數 (選擇性)
}
